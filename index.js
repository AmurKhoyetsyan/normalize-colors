const normalizeColorName = (name) => {
    const key = name.toLowerCase();
    return colorTypes[key] ?? name;
};

const componentToHex = (c) => {
    const hex = c.toString(16);
    return hex.length === 1 ? '0' + hex : hex;
};

const rgbToHex = (r, g, b) => {
    return `#${componentToHex(r)}${componentToHex(g)}${componentToHex(b)}`;
};

const parseRgb = (color) => {
    const values = color
        .replace(/rgba?\(/, '')
        .replace(')', '')
        .split(',')
        .map(v => v.trim());

    const r = parseInt(values[0]);
    const g = parseInt(values[1]);
    const b = parseInt(values[2]);

    if ([r, g, b].some(isNaN)) return '#000000';

    return rgbToHex(r, g, b);
};

const hslToHex = (h, s, l) => {
    s /= 100;
    l /= 100;

    const c = (1 - Math.abs(2 * l - 1)) * s;
    const x = c * (1 - Math.abs(((h / 60) % 2) - 1));
    const m = l - c / 2;

    let r = 0, g = 0, b = 0;

    if (0 <= h && h < 60) [r, g, b] = [c, x, 0];
    else if (60 <= h && h < 120) [r, g, b] = [x, c, 0];
    else if (120 <= h && h < 180) [r, g, b] = [0, c, x];
    else if (180 <= h && h < 240) [r, g, b] = [0, x, c];
    else if (240 <= h && h < 300) [r, g, b] = [x, 0, c];
    else if (300 <= h && h < 360) [r, g, b] = [c, 0, x];

    const rr = Math.round((r + m) * 255);
    const gg = Math.round((g + m) * 255);
    const bb = Math.round((b + m) * 255);

    return rgbToHex(rr, gg, bb);
};

const parseHsl = (color) => {
    const values = color
        .replace(/hsla?\(/, '')
        .replace(')', '')
        .split(',')
        .map(v => v.trim().replace('%', ''));

    const h = parseInt(values[0]);
    const s = parseInt(values[1]);
    const l = parseInt(values[2]);

    if ([h, s, l].some(isNaN)) return '#000000';

    return hslToHex(h % 360, s, l);
};

const normalizeColor = (color) => {
    if (!color) return '#000000';

    if (color.startsWith('#')) {
        if (color.length === 4) {
            return (
                '#' +
                color[1] + color[1] +
                color[2] + color[2] +
                color[3] + color[3]
            );
        }
        return color;
    }

    if (color.startsWith('rgb')) {
        return parseRgb(color);
    }

    if (color.startsWith('hsl')) {
        return parseHsl(color);
    }

    return normalizeColorName(color);
};


